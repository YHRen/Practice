class Solution {
  public:
    vector<vector<int>> largeGroupPositions(string S) {
      vvi ans;
      int l = 0;
      int n = S.size();
      for(int i = 0; i < n-1; /* */){
        if( S[i+1] != S[i] ) ++i;
        else{
        int j = find_if_not(S.begin() + i + 1, S.end(), [y=S[i]](char c){ return c == y; } ) - S.begin();
        if( j - i >= 3) ans.pb({i,j-1});
        i = j;
        }
      }
      return ans;
    }
};
