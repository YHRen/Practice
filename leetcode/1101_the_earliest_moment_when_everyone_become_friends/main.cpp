struct UF {
	vi e;
	UF(int n) : e(n, -1) {}
	bool same_set(int a, int b) { return find(a) == find(b); }
	int size(int x) { return -e[find(x)]; }
	int find(int x) { return e[x] < 0 ? x : e[x] = find(e[x]); }
	void join(int a, int b) {
		a = find(a), b = find(b);
		if (a == b) return;
		if (e[a] > e[b]) swap(a, b);
		e[a] += e[b]; e[b] = a;
	}
};
class Solution {

  public:
    int earliestAcq(vector<vector<int>>& logs, int N) {
      sort(all(logs), [](const vi & lhs, const vi & rhs){
          if( lhs[0] < rhs[0] ) return true;
          else return false;});
      auto uf = UF(N);
      int  res;
      for(auto & v : logs ){
        uf.join( v[1], v[2] );
        if( uf.size(0) == N ) return v[0];
      }
      return -1;
    }
};
