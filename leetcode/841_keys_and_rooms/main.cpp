class Solution {
  public:
    bool canVisitAllRooms(vector<vector<int>>& rooms) {
      int n = rooms.size();
      vi v(n,0);
      v[0] = 1;
      queue<int> Q;
      Q.push(0);
      while(!Q.empty()){
        int x = Q.front(); Q.pop();
        for(auto y : rooms[x]){
          if( v[y] == 0 ){
            v[y] = 1;
            Q.push(y);
          }
        }
      }
      return count_if( all( v ), [](int x ) {return x;} ) == n;
    }
};
