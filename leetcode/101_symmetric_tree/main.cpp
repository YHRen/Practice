/**
 * Definition for a binary tree node.
 */
struct TreeNode {
  int val;
  TreeNode *left;
  TreeNode *right;
  TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};
 
class Solution {
  private:
    bool try_compare( TreeNode * left, TreeNode * right ){
      if( left == nullptr && right == nullptr ){
        return true;
      }else if( left == nullptr || right == nullptr ){
        return false;
      }else if( left ->val == right->val ){
        return try_compare( left->left, right->right ) && 
          try_compare(left->right, right->left);
      }else{
        return false;
      }
    }
    bool bfs(TreeNode * root){
        TreeNode *left, *right;
        if (!root) return true;
        queue<TreeNode*> q1, q2;
        q1.push(root->left);
        q2.push(root->right);
        while (!q1.empty() && !q2.empty()){
            left = q1.front();
            q1.pop();
            right = q2.front();
            q2.pop();
            if (NULL == left && NULL == right)
                continue;
            if (NULL == left || NULL == right)
                return false;
            if (left->val != right->val)
                return false;
            q1.push(left->left);
            q1.push(left->right);
            q2.push(right->right);
            q2.push(right->left);
        }
        return true;
    }
public:
    bool isSymmetric(TreeNode* root) {
      if( root == nullptr ) return true;
      return try_compare(root->left, root->right);
    }
};
