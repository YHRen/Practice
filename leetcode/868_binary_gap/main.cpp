class Solution {
public:
    int binaryGap(int N) {
      string str = bitset<32>(N).to_string();
      int l = str.find('1');
      int n = str.size();
      if( l == string::npos ) return 0;
      int ans = 0;
      for(/* */; l < n-1; /* */){
        int r = str.find('1',l+1);
        if ( r == string::npos ) break;
        ans = max(ans, r-l);
        l = r;
      }
      return ans;
    }
};
