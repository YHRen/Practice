unordered_set<ll> factorize( ll n ){
  unordered_set<ll> bag;
  if( n%2 == 0 ) bag.insert(2);
  while( n%2 == 0 ){ n /= 2; }
  for(ll i = 3; i*i <= n; ++i ){
    while( n%i == 0 ){
      bag.insert(i);
      n /= i;
    }
  }
  if( n > 2 ) bag.insert(n);
  return bag;
}
